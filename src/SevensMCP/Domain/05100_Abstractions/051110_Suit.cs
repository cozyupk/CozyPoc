    /// <summary>
    /// Represents the four suits in a standard deck of playing cards.
    /// </summary>
    /// <remarks>The suits are Club, Diamond, Heart, and Spade. This enumeration is commonly used in card game
    /// implementations to identify and differentiate between the suits of playing cards.</remarks>
    public enum Suit
    {
        Club,
        Diamond,
        Heart,
        Spade
    }
}
